DROP TABLE IF EXISTS P_SLACK_MESSAGES;

CREATE TABLE P_SLACK_MESSAGES
(
    SLACK_MESSAGE_ID   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SLACK_MESSAGE_UUID VARCHAR(36)  NOT NULL UNIQUE,
    RECEIVER_SLACK_ID  VARCHAR(255) NOT NULL,
    RECEIVER_SLACK_EMAIL  VARCHAR(255) NOT NULL,
    TEXT               TEXT         NOT NULL,
    IS_PUBLIC          BOOLEAN,
    IS_DELETED         BOOLEAN,
    CREATED_AT         TIMESTAMP(6),
    CREATED_BY         VARCHAR(255),
    UPDATED_AT         TIMESTAMP(6),
    UPDATED_BY         VARCHAR(255),
    DELETED_AT         TIMESTAMP(6),
    DELETED_BY         VARCHAR(255)
);
